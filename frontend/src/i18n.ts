export function localizeBackendText(text: string, locale: Locale) {
  if (locale === "en") return text;
  const traceRunMatch = text.match(/^The agent planned the run, screened (\d+) candidates, and produced (\d+) Go, (\d+) Hold, and (\d+) No-Go decisions\.$/);
  if (traceRunMatch) {
    return `에이전트가 실행 계획을 세우고 후보 ${traceRunMatch[1]}개를 선별해 Go ${traceRunMatch[2]}개, 검토 보류 ${traceRunMatch[3]}개, 중단 권고 ${traceRunMatch[4]}개를 만들었습니다.`;
  }
  const criticSummaryMatch = text.match(/^Critic review changed the interpretation from a simple score into a gated decision: (\d+) critic finding\(s\) were recorded\.$/);
  if (criticSummaryMatch) {
    return `Critic 검토가 단순 점수를 gate 기반 판정으로 바꾸었고, 지적 사항 ${criticSummaryMatch[1]}개를 기록했습니다.`;
  }
  const evidenceSummaryMatch = text.match(/^Evidence mode was (.*); (\d+) fallback or tool-warning event\(s\) were logged\.$/);
  if (evidenceSummaryMatch) {
    return `근거 상태는 ${evidenceSummaryMatch[1]}이며, 대체 경로 또는 도구 경고 ${evidenceSummaryMatch[2]}개가 기록되었습니다.`;
  }
  const decisionImpactMatch = text.match(/^Critic review introduced (\d+) review signal\(s\), preventing unsupported confident decisions\. Redesign created (\d+) child candidate\(s\) for re-evaluation\. Final triage left (\d+) Hold and (\d+) No-Go candidate\(s\) for review or correction\.$/);
  if (decisionImpactMatch) {
    return `Critic 검토가 검토 신호 ${decisionImpactMatch[1]}개를 추가해 근거 없는 확신 판정을 막았습니다. 재설계는 재평가용 자식 후보 ${decisionImpactMatch[2]}개를 만들었고, 최종적으로 검토 보류 ${decisionImpactMatch[3]}개와 중단 권고 ${decisionImpactMatch[4]}개가 남았습니다.`;
  }
  const alertCountMatch = text.match(/^(\d+) alert\(s\), (\d+) severe blocker\(s\)\.$/);
  if (alertCountMatch) {
    return `구조 alert ${alertCountMatch[1]}개, severe blocker ${alertCountMatch[2]}개가 기록되었습니다.`;
  }
  const alertMatch = text.match(/^(\d+) structural alert\(s\) found; not a toxicity conclusion, but review is required\.$/);
  if (alertMatch) return `구조 alert ${alertMatch[1]}개가 발견되었습니다. 독성 결론은 아니지만 검토가 필요합니다.`;
  const severeMatch = text.match(/^(\d+) severe structural alert\(s\) found\.$/);
  if (severeMatch) return `Severe structural alert ${severeMatch[1]}개가 발견되었습니다.`;
  return BACKEND_TEXT_KO[text] ?? text;
}
